-- –°–æ–∑–¥–∞–Ω–∏–µ –ø–∞–ø–∫–∏ FfAdd
local function create_folder(foldername)
  if not foldername or foldername == "" then
    print("Please provide a folder name")
    return
  end

  -- –§–æ—Ä–º–∏—Ä—É–µ–º –ø–æ–ª–Ω—ã–π –ø—É—Ç—å –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ —Ç–µ–∫—É—â–µ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
  local path = vim.fn.getcwd() .. "/" .. foldername
  local cmd

  -- –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∫–æ–º–∞–Ω–¥—É –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –û–°
  local os_name = vim.loop.os_uname().sysname
  if os_name == "Windows_NT" then
    cmd = 'mkdir "' .. path .. '"'
  else -- Linux, macOS
    cmd = 'mkdir -p "' .. path .. '"'
  end

  local success = os.execute(cmd)
  if success then
    print("–°–æ–∑–¥–∞–Ω–∞ –ø–∞–ø–∫–∞: " .. path)
  else
    print("–û—à–∏–±–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è –ø–∞–ø–∫–∏: " .. path)
  end
end

-- –£–¥–∞–ª–µ–Ω–∏–µ –ø–∞–ø–∫–∏ FfDelete
local function delete_folder(foldername)
  if not foldername or foldername == "" then
    print("Please provide a folder name")
    return
  end

  -- –§–æ—Ä–º–∏—Ä—É–µ–º –ø–æ–ª–Ω—ã–π –ø—É—Ç—å –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ —Ç–µ–∫—É—â–µ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
  local path = vim.fn.getcwd() .. "/" .. foldername
  local cmd

  -- –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∫–æ–º–∞–Ω–¥—É –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –û–°
  local os_name = vim.loop.os_uname().sysname
  if os_name == "Windows_NT" then
    cmd = 'rmdir /s /q "' .. path .. '"'
  else -- Linux, macOS
    cmd = 'rm -rf "' .. path .. '"'
  end

  local success = os.execute(cmd)
  if success then
    print("–£–¥–∞–ª–µ–Ω–∞ –ø–∞–ø–∫–∞: " .. path)
  else
    print("–û—à–∏–±–∫–∞ —É–¥–∞–ª–µ–Ω–∏—è –ø–∞–ø–∫–∏: " .. path)
  end
end

