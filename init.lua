-- –°–±–æ—Ä–∫–∞ –ø—Ä–æ–µ–∫—Ç–∞ —á–µ—Ä–µ–∑ –∫–æ–º–∞–Ω–¥—É Build
-- –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Å–±–æ—Ä–∫–∏ —Ç–µ–∫—É—â–µ–≥–æ —Ñ–∞–π–ª–∞ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —è–∑—ã–∫–∞
function BuildCurrentFile()
  local filetype = vim.bo.filetype
  local cmd = ""

  if filetype == "go" then
    cmd = "go build %"
  elseif filetype == "python" then
    cmd = "echo 'Python –Ω–µ —Ç—Ä–µ–±—É–µ—Ç —Å–±–æ—Ä–∫–∏'"
  elseif filetype == "java" then
    cmd = "javac %"
  elseif filetype == "cs" then
    cmd = "dotnet build"
  else
    print("–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Ç–∏–ø —Ñ–∞–π–ª–∞: " .. filetype)
    return
  end

