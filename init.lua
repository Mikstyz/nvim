vim.api.nvim_create_user_command("Brw", function(opts)
    local query = table.concat(opts.fargs, " ")
    local url = "https://www.google.com/search?q=" .. vim.fn.escape(query, "#&")
    local open_cmd

    -- –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∫–æ–º–∞–Ω–¥—É –¥–ª—è –æ—Ç–∫—Ä—ã—Ç–∏—è –±—Ä–∞—É–∑–µ—Ä–∞ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –û–°
    if vim.fn.has("mac") == 1 then
        open_cmd = "open '" .. url .. "'"
    elseif vim.fn.has("unix") == 1 then
        open_cmd = "xdg-open '" .. url .. "'"
    elseif vim.fn.has("win32") == 1 then
        open_cmd = "start " .. url
    else
        print("–ù–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç—Å—è –Ω–∞ –≤–∞—à–µ–π –û–°")
        return
    end

    os.execute(open_cmd)
end, { nargs = "+" })




